package cn.Bean.util;

/**
 * 主力户型属性
 */
public class MainHouseValue {
	

	private String icon;//户型url
	private String image;//户型大图
	private String title;//标题
	private String area;//面积
	private String toward;//朝向
	private String modedesc;//户型描述
	private String camera;//摄像头
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getToward() {
		return toward;
	}
	public void setToward(String toward) {
		this.toward = toward;
	}
	public String getModedesc() {
		return modedesc;
	}
	public void setModedesc(String modedesc) {
		this.modedesc = modedesc;
	}
	public String getCamera() {
		return camera;
	}
	public void setCamera(String camera) {
		this.camera = camera;
	}
}
