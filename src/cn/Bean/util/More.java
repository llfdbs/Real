package cn.Bean.util;

import java.util.List;

public class More {
	private String moreName;
	private String key;
	private List<MoreValue> detail;
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getMoreName() {
		return moreName;
	}
	public void setMoreName(String moreName) {
		this.moreName = moreName;
	}
	public List<MoreValue> getDetail() {
		return detail;
	}
	public void setDetail(List<MoreValue> detail) {
		this.detail = detail;
	}
	
	
}
